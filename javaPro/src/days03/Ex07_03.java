package days03;

public class Ex07_03 {

	public static void main(String[] args) {
		int i = 10, j = 3;
		
		System.out.println( i > j);
		System.out.println( i < j);
		System.out.println( i >= j);
		System.out.println( i <= j);

		// i j 같다
		System.out.println( i == j); // ==같다
		System.out.println( i != j); // !=다르다
		
		
		
	}

}
